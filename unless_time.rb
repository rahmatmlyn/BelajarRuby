# Unless merupakan kebalikan dari if yang harus berisi true
# yaitu kondisi dari unless ini harus berisi false agar bisa masuk ke hal yang akan kita lakukan

angka = 34

unless angka == 345
    puts "di dalam unless"

else
    puts "di luar unless"
end

unless angka > 34
puts "Nilai unless"
else
puts "Nilai benar"
end